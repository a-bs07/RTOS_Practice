/*
 *xTaskNotifyFromISR(...)

This sends a notification to a task (pointed to by pcurrentHandle).

It's the ISR-safe version of xTaskNotify().

The last argument &pxHigherPriorityTaskWoken is a pointer to a flag that FreeRTOS sets to pdTRUE if the notified task has higher priority than the task currently running.

portYIELD_FROM_ISR(pxHigherPriorityTaskWoken)

If the notified task is higher priority, this macro tells the scheduler to switch context immediately after the ISR finishes, so that high-priority task can start running right away.

If pxHigherPriorityTaskWoken is pdFALSE, no context switch happens.

🧠 Why this is needed:
In an ISR, FreeRTOS doesn’t switch tasks automatically.

So, if you unblock a higher priority task, and want it to run immediately, you must explicitly request a context switch using portYIELD_FROM_ISR().
 */
 void timerInterruptHandler(void)
 {
	 BaseType_t pxHigherPriorityTaskWoken=pdFAIL;

	 xTaskNotifyFromISR(*(pcurrentHandle),0,eNoAction,&pxHigherPriorityTaskWoken);
	 portYIELD_FROM_ISR(pxHigherPriorityTaskWoken);// requesting context switching manually

 }
/* USER CODE END PV */

